package com.vkstech.leetcode.algorithms.easy.arrays;

public class ArrayUtils {

    public static void printArr(Object[] arr){
        for (Object o : arr){
            System.out.print(o + ", ");
        }
        System.out.println();
    }

    public static void printArr(int[] arr){
        for (Object o : arr){
            System.out.print(o + ", ");
        }
        System.out.println();
    }
}
